public class Relational {
    public static void main(String[] args) {
        float a=5.1f;
        float b=4.3f;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
    }
    
}
