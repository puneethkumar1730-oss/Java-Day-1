public class Arthematic {
    public static void main(String[] args) {
        int a=16;
        int b=10;
        System.out.println(a+b);
        int sum=a+b;
        System.out.println(sum);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
    
}
